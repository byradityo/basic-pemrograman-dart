void main(){
  int pil = 1;
  switch(pil){
    case 1: {
      print("Satu");
      break;
    }
    case 2: {
      print("Dua");
      break;
    }
    case 3: {
      print("Tiga");
      break;
    }
    default: {
      print("Tidak ada pilihan");
    }
  }
}