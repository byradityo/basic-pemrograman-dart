void main(){
  int a = 10;
  //jika kondisi memenuhi
  if(a < 10){
    print("Dibawah sepuluh");
  } 
  //kondisi lain jika terpenuhi
  else if (a == 10){
    print("Sepuluh");
  } 
  //kondisi lain jika terpenuhi
  else if (a >= 10){
    print("Lebih dari sepuluh");
  }
}