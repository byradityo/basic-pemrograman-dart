void main(){
  int a = 9;
  int b = 1;
  //lebih besar dari
  print(a > b);
  //lebih besar dari sama dengan
  print(a >= b);
  //lebih kecil dari
  print(a < b);
  //lebih kecil dari sama dengan
  print(a <= b);
  //sama dengan
  print(a==b);
  //tidak sama dengan
  print(a!=b);
}