  bool a = true;
  bool b = false;
  bool c = true;
  bool and = a && b;
  bool and2 = a && c;
  bool or = a | b;
  bool not = !a;
  print("AND = $and\nAND2 = $and2\nOR = $or\nNOT = $not");
}
